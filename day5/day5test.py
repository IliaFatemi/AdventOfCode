assert cratify([['[C]', '', '', '', '', '', '', '', ''], []]) == [['[C]', '', '']]
assert cratify([['[Q]','','','','','','','','','','[N]','','','','','','','','','','','','','','[N]','','','',''], []]) == [['[Q]', '', '', '[N]', '', '', '', '[N]', '']]


