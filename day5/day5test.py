assert cratify([['', '', '', '', '[D]', '', '', '', ''], []]) == [['', '[D]', '']]
assert cratify([['[N]', '[C]', '', '', '', ''], []]) == [['[N]', '[C]', '']]
assert cratify([['[Z]', '[M]', '[P]'], []]) == [['[Z]', '[M]', '[P]']]
