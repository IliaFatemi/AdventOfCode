from day5 import cratify

assert cratify([['', '', '', '', '[D]', '', '', '', ''], []]) == [['', '[D]', '']]
assert cratify([['[N]', '[C]', '', '', '', ''], []]) == [['[N]', '[C]', '']]
assert cratify([['[Z]', '[M]', '[P]'], []]) == [['[Z]', '[M]', '[P]']]
assert cratify([['', '', '', '', '', '', '', '', '[C]'], []]) == [['', '', '[C]']]
assert cratify([['[C]', '', '', '', '', '', '', '', ''], []]) == [['[C]', '', '']]